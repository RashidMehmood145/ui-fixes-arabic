											<p class="rtl">Malware is a category of malicious code that includes viruses, worms, and Trojan horses.</p>
											<h3 class="threat-malware-heading rtl">Types of Malware:<span class="yellow-behind-line"></span></h3>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Trojans<span></h4>
											<p class="rtl">This is a malware in which malicious or harmful code is contained inside apparently harmless programming or data in such a way that it can get control and do its chosen form of damage. The main goal for the Trojan is to obtain your personal details without your knowledge.</p>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Virus<span></h4>
											<p class="rtl">This is a malware that can change computer operations, without the permission or knowledge of the user. This program can also replicate itself and spread over a computer network.</p>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Worm<span></h4>
											<p class="rtl">This is a malware that reproduces itself so that it can infect other computers. Frequently, it uses a computer network to spread itself, relying on security failures on the target computer without you being aware that your machine has become infected.</p>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Spyware<span></h4>
											<p class="rtl">This is a malware that aims to gather information about a person or organization without their knowledge and that may send such information to another entity without the user’s knowledge. It can spy on how you’re using your computer – for example, by tracking the data you enter via your keyboard, taking screen shots or getting a list of running applications.</p>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Ransomware<span></h4>
											<p class="rtl">This is a malware that modifies data on your computer – so that your computer doesn’t run correctly or you can no longer use specific data. The criminal will only restore your computer’s performance or unblock your data, after you have paid them the ransom money that they demand.</p>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Rootkit<span></h4>
											<p class="rtl">A rootkit designed to conceal certain objects or activities in your system. Often their main purpose is to prevent malicious programs being detected – in order to extend the period in which programs can run on an infected computer</p>
											<h3 class="threat-malware-heading rtl">Spreading of Malware<span class="yellow-behind-line-short"></span></h3>
											<p class="rtl">Malware is spread in several ways and there are continual and innovative ways that criminals use to get it onto your computer. The most commonly known ways include the following:</p>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Emails<span></h4>
											<p class="rtl">Often, especially recently, malware has been spreading through emails. It may be present in the attachments, or downloaded when you click on a link within the email.</p>
											<h3 class="malware-sub-heading rtl">How to be protected:</h3>
											<ul class="rtl">
												<li class="rtl">You must be aware and suspicious of attachments or links embedded within the email, even if it is coming from someone you know. Always scan your emails and attachments.</li>
												<li class="rtl">If you do not know the sender, then this is a valid reason to never open the attachments or click on a link within the email.</li>
											</ul>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Websites<span></h4>
											<p class="rtl">Websites are also common places where malware can be inadvertently downloaded. Visiting a website and accepting the installation of free add-ons or applications might automatically download a malware without you knowing that.</p>
											<h3 class="malware-sub-heading rtl">How to be protected:</h3>
											<ul class="rtl">Always be suspicious of such requests, especially those known as ActiveX components or strange media players</ul>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Pop-ups<span></h4>
											<p class="rtl">This is a way to spread malware through the use of pop-up advertisements or false windows pop-up alerts or messages. Such pop-ups appear to look exactly as Windows alert pop-ups, and might fool you into believing it to be a legitimate Windows alert.</p>
											<h3 class="malware-sub-heading rtl">How to be protected:</h3>
											<ul class="rtl">
												<li class="rtl">You should never click links or images appear in pop-up messages.</li>
												<li class="rtl">Always be caution of pop-up hoax advertisements such as those that claim to have scanned your computer and detected malware</li>											
											</ul>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Computer storage media<span></h4>
											<p class="rtl">Sharing computer storage media, such as DVDs, CDs, and USB drives, can easily spread malware into your computer.</p>
											<h3 class="malware-sub-heading rtl">How to be protected:</h3>
											<ul class="rtl">
												<li class="rtl">Never insert storage media and USB in untrusted computer such as public computer in airports and business centers.</li>
												<li class="rtl">You should always scan files on storage media before copying or opening them.</li>
											</ul>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Social networking sites<span></h4>
											<p class="rtl">Social networking sites are becoming a main source of malware by installing malicious third party add-on applications when you inadvertently allow it or when clicking on web links in messages. You must be cautious when accessing these sites, as they give a false sense of security.</p>
											
											<h3 class="malware-sub-heading rtl">How to be protected:</h3>
											<ul class="rtl">
												<li class="rtl">Only install third party social networking applications that are well known and trusted.</li>
												<li class="rtl">Never click links in messages from contacts that you do not know.</li>
											</ul>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Mobile devices<span></h4>
											<p class="rtl">Mobile devices, particularly mobile phones, are also becoming sources for spreading malware. As these devices become more powerful, it will certainly lead to the spread of malware.</p>
											<h3 class="malware-sub-heading rtl">How to be protected:</h3>
											<ul class="rtl">
												<li class="rtl">You should never install unverified or unsigned software on your mobile device.</li>
												<li class="rtl">Visit official website for the mobile applications owner to get the official internet link to download the official app i.e. you can visit Alistithmar Capital website to find the official links to download our official online trading apps.</li>
											</ul>
											<h4 class="heading-box rtl"><span class="span-square -box"></span><span class="heading-box-text">Software<span></h4>
											<p class="rtl">Legitimate looking software is one of the most common methods to spread malware. Pirated software, such as those obtained through Torrents, often facilitates the spread of malware.</p>
											<h3 class="malware-sub-heading rtl">How to be protected:</h3>
											<ul class="rtl">
												<li class="rtl">You should never install unlicensed or unapproved software on your computer</li>
												<li class="rtl">You should be suspicious of all free software.</li>
												<li class="rtl">Keeping your operating system, software products and Web browsers up-to-date with the latest security patches</li>
											</ul>
